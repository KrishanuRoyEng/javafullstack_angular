package DiamondProblem;

public class DiamondProblemDemo {

	public static void main(String[] args) {
		System.out.println("If Java allowed multiple inheritance with classes...");
		System.out.println("Class D would inherit from both B and C.");
		System.out.println("Both B and C override the print() method from A.");
		System.out.println("Which version should D use? B's or C's?");
		System.out.println("This ambiguity is called the Diamond Problem.");

	}

}
