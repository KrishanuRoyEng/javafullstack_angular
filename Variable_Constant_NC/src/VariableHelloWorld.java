/**
 * Hello World with variables demonstrating:
 * - Variable declaration and initialization
 * - Different data types
 * - String concatenation
 */

public class VariableHelloWorld {
  public static void main(String[] args) {
    System.out.println(" This is The Demonstration of Variable Declaration");
    // Declare and initialize variables
    String greeting = "Hello";
    String target = "World";
    int year = 2024;
    boolean isLearning = true;

    // Display information using variables
    System.out.println(greeting + ", " + target + "!");
    System.out.println("Current year: " + year);
    System.out.println("Are we learning Java? " + isLearning);

    // Demonstrate different data types
    char initial = 'J';
    double version = 17.0;
    System.out.println("Java version: " + version);
    System.out.println("Language starts with: " + initial);
  }
}
